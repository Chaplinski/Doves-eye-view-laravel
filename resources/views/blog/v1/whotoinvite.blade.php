                                        <p class="q">Let’s start with your in-person guestlist. It likely includes the people who are closest to you. Long time friends and family are generally on this list, and some couples lists include friends and family members who live hundreds or thousands of miles away. Although some of these people may be able to make it to your wedding, in all likeliness you will not have 100% of these people in attendance. Including on your wedding invitation the fact that you will be having a livestream and sharing the link to your personalized livestream webpage will help put your guests at ease knowing that they can still be a part of your big day even though they cannot be there in person. We have seen couples include the livestream link directly on their invitation, and others have included a separate insert with their invitation detailing how to access the livestream.</p>
